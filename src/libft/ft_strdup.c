#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*res;

	res = ft_zalloc((ft_strlen(s1) + 1) * sizeof(char));
	if (res == NULL)
		return (NULL);
	ft_strlcpy(res, s1, ft_strlen(s1) + 1);
	return (res);
}
